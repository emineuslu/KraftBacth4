package day04_VariableCont;

public class NarrowingCasting {
    public static void main(String[] args) {

        double rate = 3.9;

        int num=10;
         num= (int)rate;  // double tipinde 3.9 sayısını int kısmını al dıye talımat verdik
        // type dönüşümü manual Casting

        System.out.println(num);


    }



}
